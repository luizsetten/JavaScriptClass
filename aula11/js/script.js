let num1 = Number(prompt("Numero a ser somado"));
let num2 = Number(prompt("Outro numero a ser somado"));

alert(`Resultado é ${num1+num2}`);

/**
 * alert() - pop-up sem retorno contendo um alerta
 * prompt() - pop-up com campo de inserção de texto que será retornado
 * confirm() - pop-up com Ok e Cancelar retorna um boolean
 */